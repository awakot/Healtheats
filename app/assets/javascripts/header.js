function openMenu(){
  $(".navbar").css("display", "block");
  $(".openMenuBtn").css("display", "none");
  $(".closeMenuBtn").css("display", "block");
};

function closeMenu(){
  $(".navbar").css("display", "none");
  $(".openMenuBtn").css("display", "block");
  $(".closeMenuBtn").css("display", "none");
};
